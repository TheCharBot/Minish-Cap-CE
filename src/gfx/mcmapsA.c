unsigned char mcmapsA_init(void)
{
    unsigned int data, i;
    uint8_t appvar;

    appvar = ti_Open("mcmapsA", "r");
    if (appvar == 0)
    {
        return 0;
    }

    data = (unsigned int)ti_GetDataPtr(appvar) - (unsigned int)mcmapsA_appvar[0] + mcmapsA_HEADER_SIZE;
    for (i = 0; i < 1; i++)
    {
        mcmapsA_appvar[i] += data;
    }

    ti_Close(appvar);

    data = (unsigned int)mcmapsA_appvar[0] - (unsigned int)shf_tiles_compressed[0];
    for (i = 0; i < shf_tiles_num; i++)
    {
        shf_tiles_compressed[i] += data;
    }

    return 1;
}

